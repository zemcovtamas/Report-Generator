package hu.dolphio.tprttapi.model.rtt;

public class ReportProjectVO {
    private int id;
    private String name;
    private String breadcrumb;

    public ReportProjectVO() {
    }

    public ReportProjectVO(int id, String name, String breadcrumb) {
        this.id = id;
        this.name = name;
        this.breadcrumb = breadcrumb;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getBreadcrumb() {
        return breadcrumb;
    }

    public void setBreadcrumb(String breadcrumb) {
        this.breadcrumb = breadcrumb;
    }
    
    
}
